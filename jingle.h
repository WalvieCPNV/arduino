int tonePin = 6;
int ledOn;

void ledJingle()
{
    digitalWrite(ledOn, LOW);
    int randomLed = random(2, 6);

    if (randomLed == ledOn)
    {
        if (randomLed == 5)
        {
            randomLed -= 1;
        }
        else
        {
            randomLed += 1;
        }
    }

    ledOn = randomLed;
    digitalWrite(ledOn, HIGH);
}

void allLedOn(int delayTime)
{
    for (int i = 2; i < 6; i++)
    {
        digitalWrite(i, HIGH);
    }
    delay(delayTime);
    for (int i = 2; i < 6; i++)
    {
        digitalWrite(i, LOW);
    }
}

